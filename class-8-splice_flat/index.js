let color = ['red', 'black', 'white', 'blue', 'pink', 'yellow']

//indexOf() : returns first matching element's index
var x = color.indexOf('red')
console.log(x)

//lastIndexOf(): returns last matching element's index
var x = color.lastIndexOf('red')
console.log(x)

//Array.isArray() : used check whether it is array or not
var x = Array.isArray(color)
console.log(x)

var s = 'asda'
var x = Array.isArray(s)
